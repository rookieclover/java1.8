package cn.b2b.index.product.client;

import java.util.List;


public class ProductResultBean {

    private List<IProductSearchInfo> searchResultList;
    private List<ITuple> tradeids;
    private List<ITuple> industryids;
    
    private int count;
    private int clustCount;
    
    public List<IProductSearchInfo> getSearchResultList() {
        return searchResultList;
    }
    public void setSearchResultList(List<IProductSearchInfo> searchResultList) {
        this.searchResultList = searchResultList;
    }

    public List<ITuple> getTradeids() {
        return tradeids;
    }
    public void setTradeids(List<ITuple> tradeids) {
        this.tradeids = tradeids;
    }
    public List<ITuple> getIndustryids() {
        return industryids;
    }
    public void setIndustryids(List<ITuple> industryids) {
        this.industryids = industryids;
    }
    public int getCount() {
        return count;
    }
    public void setCount(int count) {
        this.count = count;
    }
    public int getClustCount() {
        return clustCount;
    }
    public void setClustCount(int clustCount) {
        this.clustCount = clustCount;
    }
    
}
